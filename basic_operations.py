# Basic Arithmetic Exercise

#Calculating sum, difference, and production and printing the results.
number1 =  10
number2 = 5

# Addition, Subtraction, and Multiplications Calculations
sum = number1 + number2 # Calculating sum of two numbers
difference = number1 - number2 # Calculating difference of two numbers
product = number1 * number2 # Calculating product of two numbers

# Calculation Results
print(f"Addition of {number1} and {number2} is {sum}")
print(f"Subtraction of {number1} and {number2} is {difference}")
print(f"Multiplication of {number1} and {number2} is {product}")